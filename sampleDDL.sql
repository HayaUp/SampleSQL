    id INT(2) UNSIGNED NOT NULL AUTO_INCREMENT,
    name VARCHAR(200) NOT NULL,
    price INT(3) UNSIGNED NOT NULL,
    price_range INT(2) UNSIGNED NOT NULL,
    PRIMARY KEY(id)
) DEFAULT CHARSET=utf8;

